pub enum TokenType {
    Illegal,
    Eof,
    Ident,
    Int,
    Assign,
    Plus,
    Minus,
    Bang,
    Asterisk,
    Slash,
    LessThen,
    GreaterThen,
    Equal,
    NotEqual,
    Comma,
    Semicolon,
    LParen,
    RParen,
    LSquirly,
    RSquirly,
    Function,
    Let,
    True,
    False,
    If,
    Else,
    Return,
}

struct Token {
    token_type: TokenType,
    literal: char,
}